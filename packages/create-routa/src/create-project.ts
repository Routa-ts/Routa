/**
 * Builds the manifest JSON for generated project files.
 *
 * @param content - File contents used to compute the manifest hashes.
 * @returns A formatted JSON string for the `.routa/manifest.json` file.
 */
function manifestJson(content: {
	route: string;
	schema: string;
	routesMetadata: string;
	baseline: string;
}): string {
	return `${JSON.stringify(
		{
			version: 1,
			openapi: {
				baseline: ".routa/openapi-baseline.json",
			},
			generated: [
				{
					path: "src/routes/status/route.ts",
					source: "openapi.yaml",
					operationIds: ["getStatus"],
					kind: "route",
					sha256: sha256(content.route),
				},
				{
					path: "src/routes/status/schemas.ts",
					source: "openapi.yaml",
					operationIds: ["getStatus"],
					kind: "schema",
					sha256: sha256(content.schema),
				},
				{
					path: ".routa/routes.gen.ts",
					source: "openapi.yaml",
					kind: "route-metadata",
					sha256: sha256(content.routesMetadata),
				},
				{
					path: ".routa/openapi-baseline.json",
					source: "openapi.yaml",
					kind: "openapi-baseline",
					sha256: sha256(content.baseline),
				},
			],
		},
		null,
		"\t",
	)}\n`;
}

/**
 * Computes the SHA-256 hash of a string.
 *
 * @param content - The input string to hash.
 * @returns The hash as a hexadecimal string.
 */
function sha256(content: string): string {
	return createHash("sha256").update(content).digest("hex");
}

/**
 * Generates the project's package manifest.
 *
 * @param name - The package name
 * @param routaVersion - The version specifier used for Routa dependencies
 * @param openApi - Whether to include OpenAPI-related scripts
 * @returns A formatted `package.json` file
 */
function packageJson(name: string, routaVersion: string, openApi: boolean): string {
	return `${JSON.stringify(
		{
			name,
			version: "0.0.0",
			private: true,
			type: "module",
			scripts: {
				dev: "routa dev",
				start: "routa start",
				check: "routa check",
				build: "routa build",
				lint: "biome check .",
				format: "biome check --write .",
				...(openApi
					? {
							scaffold: "routa scaffold openapi.yaml",
							"openapi:check": "routa openapi check",
						}
					: {}),
			},
			dependencies: {
				"@routa/cli": routaVersion,
				"@routa/core": routaVersion,
				tsx: "^4.22.4",
				zod: "^4.4.3",
			},
			devDependencies: {
				"@biomejs/biome": "^2.5.1",
				"@types/node": "^24.0.4",
				typescript: "^5.8.3",
			},
		},
		null,
		"\t",
	)}\n`;
}

