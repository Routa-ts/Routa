/**
 * Returns the default `src/routa.ts` source for a new project.
 *
 * @returns The generated module string that exports a Routa app configured for port `3000`.
 */
function routaSource(): string {
	return `import { createRouta } from "@routa/core";

export default createRouta({
\tport: 3000,
});
`;
}

/**
 * Returns the OpenAPI YAML template for the scaffolded status endpoint.
 *
 * @returns The OpenAPI 3.1 YAML document for the `GET /status` operation.
 */
function openApiYaml(): string {
	return `openapi: 3.1.0
info:
  title: Routa API
  version: 0.0.0
paths:
  /status:
    get:
      operationId: getStatus
      responses:
        "200":
          description: Service status
          content:
            application/json:
              schema:
                type: object
                required:
                  - ok
                properties:
                  ok:
                    type: boolean
`;
}

/**
 * Returns the source for the default `/status` route.
 *
 * @returns The TypeScript module text for `src/routes/status/route.ts`.
 */
function statusRouteSource(): string {
	return `import { createRoute, defineRoute } from "@routa/core";
import { GetStatusResponse } from "./schemas.js";

export default defineRoute({
\tget: createRoute({
\t\tresponses: {
\t\t\tsuccess: {
\t\t\t\tstatus: 200,
\t\t\t\tschema: GetStatusResponse,
\t\t\t},
\t\t},
\t\trun: () => ({ type: "success", data: { ok: true } }),
\t}),
});
`;
}

/**
 * Generates the `/status` response schema module source.
 *
 * @returns The TypeScript source for `GetStatusResponse`.
 */
function statusSchemasSource(): string {
	return `import { z } from "zod";

export const GetStatusResponse = z.object({
\tok: z.boolean(),
});
`;
}
